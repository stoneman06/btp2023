class MyHomePage extends StatefulWidget {
  MyHomePage({Key? key, required this.title}) : super(key: key);

  final String title;

  @override
  _MyHomePageState createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  late Image _image;

  @override
  void initState() {
    super.initState();
    getData();
  }

  void getData() {
    databaseReference.child('esp32-cam/photo').onValue.listen((event) {
      if (event.snapshot.value is String) {
        final image = Image.memory(base64Decode(event.snapshot.value as String));
        setState(() {
          _image = image;
        });
      }
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(widget.title),
      ),
      body: Center(
        child: _image ?? Text('Loading...'),
      ),
    );
  }
}